def prefix_check(codes: dict) -> tuple:
    for code in codes:
        for child in codes:
            if code == child:
                continue
            if codes[code] == codes[child][:len(codes[code])]:
                return False, code, child, codes[code], codes[child]
    return True, None, None, None, None


